  // Generate built-in collection type classes
  generateCollectionTypes() {
    let code = '// Built-in Collection Types\n';
    code += '#include <vector>\n';
    code += '#include <map>\n';
    code += '#include <string>\n\n';
    
    // List class (wrapper around std::vector)
    code += `template<typename T>
class List {
private:
  std::vector<T> _data;
  
public:
  List() {}
  
  List(std::initializer_list<T> init) : _data(init) {}
  
  void push(T value) {
    _data.push_back(value);
  }
  
  T pop() {
    if (_data.empty()) return T();
    T value = _data.back();
    _data.pop_back();
    return value;
  }
  
  int length() {
    return _data.size();
  }
  
  T get(int index) {
    if (index >= 0 && index < _data.size()) {
      return _data[index];
    }
    return T();
  }
  
  void set(int index, T value) {
    if (index >= 0 && index < _data.size()) {
      _data[index] = value;
    }
  }
};

`;
    
    // Map class (wrapper around std::map)
    code += `template<typename K, typename V>
class Map {
private:
  std::map<K, V> _data;
  
public:
  Map() {}
  
  V get(K key) {
    auto it = _data.find(key);
    if (it != _data.end()) {
      return it->second;
    }
    return V();
  }
  
  void set(K key, V value) {
    _data[key] = value;
  }
  
  bool has(K key) {
    return _data.find(key) != _data.end();
  }
  
  void remove(K key) {
    _data.erase(key);
  }
  
  int size() {
    return _data.size();
  }
};

`;
    
    return code;
  }
